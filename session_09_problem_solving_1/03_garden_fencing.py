# Garden Fencing

# Measurements of the garden (in meters)
side1 = 20
side2 = 12
side3 = 12

# Cost per meter of fencing
cost_per_meter = 150

# Calculating the total perimeter
preimeter = side1 + side2 + side3

# Calculating the total cost
total_cost = preimeter * cost_per_meter

# Display the result
print(f"The total cost of fencing the garden is: ${total_cost}")
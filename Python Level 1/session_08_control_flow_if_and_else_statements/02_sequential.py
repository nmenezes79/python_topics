# Sequential execution in Python

print("Welcome to the program!")  # Step 1: Print a message
name = input("Enter your name: ")  # Step 2: Take user input
print(f"Hello, {name}!")  # Step 3: Greet the user

age = int(input("Enter your age: "))  # Step 4: Take age input
year = 2025 - age  # Step 5: Calculate birth year
print(f"You were born in {year}.")  # Step 6: Display result

print("Thank you for using the program!")  # Step 7: End message
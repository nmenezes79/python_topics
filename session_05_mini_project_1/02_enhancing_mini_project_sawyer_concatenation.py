# Initial List
previous_list = ["Apple", "Banana", "Cherry"]

# Concatenated string
new_string = "Grape"

# Append new string to the list
previous_list.append(new_string)

# Display the updated list
print("Updated List: ", previous_list)

# Sort the list in ascending order
previous_list.sort()

# Display the sorted list
print("Sorted List: ", previous_list)
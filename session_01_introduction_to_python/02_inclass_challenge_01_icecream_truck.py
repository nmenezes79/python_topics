# Define the original cost per ice-cream
original_price_per_icecream = 10 # USD

# Calculate the total cost for 2 ice-cream
discounted_price_for_two = 2 * original_price_per_icecream * 0.50

# Calculate the price per ice-cream after discount
price_per_ice_cream_after_discount = discounted_price_for_two / 2

print(f"The price for one ice-cream after discount is : ${price_per_ice_cream_after_discount:.2f} USD")
# 03_tuples_joining

# In Python, you can join (concatenate) tuples using the + operator. 
# Defining two tuples
tuple1 = (1, 2, 3)
tuple2 = ('a', 'b', 'c')

# Joining tuples using +
joined_tuple = tuple1 + tuple2

# Output the result
print(joined_tuple)

# If you need to repeat a tuple multiple times, you can use the * operator
repeated_tuple = tuple1 * 2
print(repeated_tuple)